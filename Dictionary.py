# Creating a Dictionary
# with Integer Keys
Dict = {1: 'Collins', 2: 'Mustang', 3: 'Ingland'}
print("\nDictionary with the use of Integer Keys: ")
print(Dict)

# Creating a Dictionary
# with Mixed keys
Dict = {'Name': 'Collins', 1: [5, 4, 7, 33]}
print("\nDictionary with the use of Mixed Keys: ")
print(Dict)
